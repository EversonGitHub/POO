package Usuarios;

public class Usuarios {
	private String nome;
	private String sobrenome;
	
	public String getNome(){
		return nome;
	}
	
	public void setNome(String n){
		this.nome = n;
	}
	
	public String getSobrenome(){
		return sobrenome;
	}
	
	public void setSobrenome(String s){
		this.sobrenome = s;
	}
}